// 완본체 팩토리 — 같은 상세 페이지 구조를 재사용하는 예시 완본체.
// 보일러플레이트를 줄이려고 팩토리로 생성한다. (가격·구성은 예시값)
function buildPC(id, category, o) {
  return {
    id,
    category,
    name: o.name,
    subtitle: o.subtitle,
    // 기본은 자기 id 사진. img 가 지정되면(신규 구성) 스타일이 맞는 기존 빌드 사진을 재사용.
    image: `/images/builds/${o.img ?? id}.jpg`,
    price: o.price,
    tag: o.tag,
    reviews: o.reviews,
    cpu: o.cpu,
    gpu: o.gpu,
    ram: o.ram,
    ssd: o.ssd,
    specs: [
      ['CPU', o.cpuFull],
      ['쿨러', o.cooler],
      ['메인보드', o.mb],
      ['메모리', o.ram],
      ['그래픽카드', o.gpuFull],
      ['SSD', o.ssd],
      ['파워', o.psu],
      ['케이스', o.case],
    ],
    description: o.description,
    // 이 구성만의 컴친 팁 포인트(있으면 상세 페이지 팁 맨 앞에 노출)
    point: o.point,
  }
}

